# ---------------------------------------------------------------------------
# Deterministic UUIDs (namespace-based) so that stacking rules can cross-
# reference other programs and re-runs are idempotent.
# ---------------------------------------------------------------------------
NS = uuid.UUID("a1b2c3d4-e5f6-7890-abcd-ef1234567890")


def _id(slug: str) -> uuid.UUID:
    return uuid.uuid5(NS, slug)


NOW = datetime.now(timezone.utc)

# ---------------------------------------------------------------------------
# Helper to build an incentive dict matching the incentive_programs table
# ---------------------------------------------------------------------------

def _program(
    slug: str,
    *,
    name: str,
    type: str,
    source_authority: str,
    geographic_scope: str,
    eligible_states: list[str] | None = None,
    eligible_zips: list[str] | None = None,
    vehicle_criteria: dict | None = None,
    buyer_criteria: dict | None = None,
    incentive_value_type: str,
    incentive_amount: float | None = None,
    incentive_max_amount: float | None = None,
    incentive_percentage: float | None = None,
    start_date: str,
    end_date: str | None = None,
    application_deadline: str | None = None,
    funding_status: str = "open",
    claim_mechanism: str,
    source_url: str,
    confidence_score: float = 0.8,
    is_active: bool = True,
) -> dict:
    return {
        "id": _id(slug),
        "name": name,
        "type": type,
        "source_authority": source_authority,
        "geographic_scope": geographic_scope,
        "eligible_states": eligible_states or [],
        "eligible_zips": eligible_zips or [],
        "vehicle_criteria": vehicle_criteria or {},
        "buyer_criteria": buyer_criteria or {},
        "incentive_value_type": incentive_value_type,
        "incentive_amount": incentive_amount,
        "incentive_max_amount": incentive_max_amount,
        "incentive_percentage": incentive_percentage,
        "stackable_with": [],
        "mutually_exclusive_with": [],
        "start_date": datetime.fromisoformat(start_date).replace(tzinfo=timezone.utc),
        "end_date": datetime.fromisoformat(end_date).replace(tzinfo=timezone.utc) if end_date else None,
        "application_deadline": datetime.fromisoformat(application_deadline).replace(tzinfo=timezone.utc) if application_deadline else None,
        "funding_status": funding_status,
        "claim_mechanism": claim_mechanism,
        "last_verified": NOW,
        "source_url": source_url,
        "confidence_score": confidence_score,
        "is_active": is_active,
        "created_at": NOW,
        "updated_at": NOW,
    }


