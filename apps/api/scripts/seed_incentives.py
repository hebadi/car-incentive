# ============================================================================
# STACKING RULES
# After all programs are defined, set up mutual exclusion relationships.
# Cash rebates and 0% APR from the same manufacturer are mutually exclusive.
# ============================================================================


def _apply_stacking_rules(programs: list[dict]) -> None:
    """
    Mark mutual exclusion between 0% APR and cash rebates for each OEM.
    State + manufacturer + utility + affinity are all stackable (default).
    30C charger credit is stackable with all vehicle incentives (default).
    """
    apr_ids = {
        "Stellantis": [],
        "Hyundai USA": [],
        "Hyundai": [],
        "Volkswagen USA": [],
        "Volkswagen": [],
        "Nissan USA": [],
        "Nissan": [],
        "Tesla": [],
        "General Motors": [],
        "Toyota USA": [],
    }
    cash_ids = {
        "Stellantis": [],
        "Hyundai USA": [],
        "Hyundai": [],
        "Volkswagen USA": [],
        "Volkswagen": [],
        "Nissan USA": [],
        "Nissan": [],
        "Tesla": [],
        "General Motors": [],
        "Toyota USA": [],
    }

    for p in programs:
        authority = p["source_authority"]
        if p["incentive_value_type"] == "rate_reduction" and authority in apr_ids:
            apr_ids[authority].append(p["id"])
        elif (
            p["type"] == "manufacturer"
            and p["incentive_value_type"] == "fixed"
            and authority in cash_ids
        ):
            cash_ids[authority].append(p["id"])

    by_id = {p["id"]: p for p in programs}
    for authority in apr_ids:
        for apr_id in apr_ids[authority]:
            for cash_id in cash_ids.get(authority, []):
                by_id[apr_id]["mutually_exclusive_with"].append(cash_id)
                by_id[cash_id]["mutually_exclusive_with"].append(apr_id)


_apply_stacking_rules(ALL_PROGRAMS)


